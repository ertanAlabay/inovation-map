interface Location {
    name: string;
    coords: [number, number];
    group: string; // Grup bilgisi
}

interface MapComponentProps {
    locations: Location[];
}

// Grup rengine göre ikon oluşturma fonksiyonu
const getIcon = (group: string) => {
    const color = group === 'group1' ? 'red' : group === 'group2' ? 'blue' : 'green'; // Grup rengine göre renkler
    return new L.Icon({
        iconUrl: `https://raw.githubusercontent.com/pointhi/leaflet-color-markers/master/img/marker-icon-${color}.png`,
        shadowUrl: require('leaflet/dist/images/marker-shadow.png'),
        iconSize: [25, 41], // Simgelerin boyutu
        iconAnchor: [12, 41], // Simgelerin konumlanma noktası
        popupAnchor: [1, -34],
        shadowSize: [41, 41],
    });
};

const MapComponent: React.FC<MapComponentProps> = ({ locations }) => {
    const onEachFeature = (feature: any, layer: any) => {
        if (feature.properties && feature.properties.name) {
            layer.bindPopup(`Ülke: ${feature.properties.name}`);
        }
    }
    useEffect(() => {
        // Leaflet icon ayarlama
        delete L.Icon.Default.prototype._getIconUrl;
        L.Icon.Default.mergeOptions({
            iconRetinaUrl: require('leaflet/dist/images/marker-icon-2x.png'),
            iconUrl: require('leaflet/dist/images/marker-icon.png'),
            shadowUrl: require('leaflet/dist/images/marker-shadow.png'),
        });
    }, []);

    return (
        <MapContainer center={[39.9334, 32.8597]} zoom={5} style={{ height: '100vh', width: '100%' }}>
            <TileLayer
    url="https://cartodb-basemaps-a.global.ssl.fastly.net/light_all/{z}/{x}/{y}.png"
    attribution='&copy; <a href="https://carto.com/">CartoDB</a>'
/>
            {locations.map((location, index) => (
                <Marker
                    key={index}
                    position={location.coords}
                    icon={getIcon(location.group)} // Grup rengine göre ikon oluştur
                >
                    <Popup>{location.name}</Popup>
                </Marker>
            ))}
            <GeoJSON data={turkeyBorders} onEachFeature={onEachFeature} style={() => ({color: 'blue', weight: 2, fillOpacity: 0.1})} />
        </MapContainer>
    );
};

export default MapComponent;
